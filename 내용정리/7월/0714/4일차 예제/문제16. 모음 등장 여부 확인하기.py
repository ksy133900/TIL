word = 'apple'
cnt=0
for i in word:
    if i == 'a':
        cnt+=1
    elif i == 'e' :
        cnt+=1
    elif i == 'i' :
        cnt+=1
    elif i == 'o' :
        cnt+=1
    elif i == 'u' :
        cnt+=1
print(cnt)

