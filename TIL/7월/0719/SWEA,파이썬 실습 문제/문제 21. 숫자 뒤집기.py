number = 1234
# 4321 = 4000+300+20+1
a = number//1000 # 1
b = (number%1000) # 나머지 234
c = ((b//100)*10) # 234//100*10 = 20
d = (((number%100)//10)*100) #1234를 100으로 나눈 나머지 34를 다시 10으로 나누고 나온 몫3에 x100 = 300
e = ((number%10)*1000) #1234를 10으로 나눈 나머지 4에 x1000 = 4000
f = (e+d+c+a) # 4000+300+20+1 = 4321

